#include "../inc/minishell.h"

//	'readline' records whatever is inputed in terminal, and returns a memory
//	allocated char *buffer
int	main(void)
{
	char	*str;

	signal_init();
	while (1)
	{
		str = readline("Minishell >$ ");
		add_history(str);
		signal_exit(str);
		free(str);
	}
}
